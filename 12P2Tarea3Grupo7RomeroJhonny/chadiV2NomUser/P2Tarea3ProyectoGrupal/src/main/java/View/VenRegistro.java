    private void JLOpRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JLOpRegistrarMouseClicked
        //Ingresar Usuario
        user.setnomb(txtnom.getText());
        user.setcorreo(txtCorreo.getText());
        user.settelef(txtTelefono.getText());
        user.setci(txtCi.getText());
        user.setcont(txtcont.getText());

        //Intancia de la base de datos
        MongoClient mongo = new MongoClient("localhost",27017);
        //Crea el nombre d ela base de datos       
        MongoDatabase database = mongo.getDatabase("Cine");
        //Crear Coleccin
        MongoCollection <Document> usercollec = database.getCollection("Usuarios");
        //Para insertar
        Document userdocument = new Document()
        .append("Usuario: ",user.getnomb())
        .append("Correo: ", user.getcorreo())
        .append("Telefono: ", user.gettelef())
        .append("Cedula: ", user.getci())
        .append("Contrase√±a: ", user.getcont());
        //GUARDANDO EL REGISTRO
        //nombrecoleccion.insertOne(nombredocumento);
        try{
            usercollec.insertOne(userdocument);
            JOptionPane.showMessageDialog(null, "Agregado Correctamente a la Base de Datos 'Cine' en la coleccion 'Usuario'", "Informacion", JOptionPane.INFORMATION_MESSAGE);
        }catch(MongoException e){
            JOptionPane.showMessageDialog(null, "Error al agregar a la base de datos en la coleccion 'Usuario'","Error", JOptionPane.ERROR_MESSAGE);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_JLOpRegistrarMouseClicked

